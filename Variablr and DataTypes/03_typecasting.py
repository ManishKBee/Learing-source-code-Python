# How find the data type and how to change/cast the data type

a="9987"
a=int(a) # data type changed to <int>
a=str(a) # data type changed to <str>
a=float(a) # data type changed to <float>
print(type(a)) # printing the data type
print(a+3) # this operation is only possible with <int> and <float> data type
