# Different type of operators in python

# 1. Arithmetic operators (+,-,*,/)
a=64
b=36
print("value of a+b", a+b)
print("value of a-b", a-b)
print("value of a*b", a*b)
print("value of a/b", a/b)

# 2. Assignment operators (+=,-=,*=,/=)
a=500
a+=500
a-=200
a*=3
a/=150
print(a)

# 3. Comparision operators
a=(10==9)
b=(10<=9)
c=(10>=9)
d=(10<9)
e=(10>9)
f=(10!=9)
print(a)
print(b)
print(c)
print(d)
print(e)
print(f)

# 4. Logical operators
bool1=True
bool2=False
print("The value of bool1 and bool2", bool1 and bool2)
print("The value of bool1 or bool2", bool1 or bool2)
print("The value of not bool2", not bool2)
