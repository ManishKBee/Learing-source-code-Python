# escape sequence character

phrase="The quick \nbrown fox jumps \tover the lazy \'dog, where's the \\fox now"

print(phrase)

'''Note: there are many escape sequence which 
we use in our string and the escape sequence
is represented by a backslash'\'. escape
sequence character comprises of more thab one 
character but represent only one character 
when used within the string'''

'''Example: 1. \n for next line
2. \t for including TAB between the words
3. \' for including a single quote (') 
before the word like if we put \'boys then 
it will show output as-'boys
4. \\ for including a backslash before word
same as \' shown in the above example and 
there are many more which we say etc...'''